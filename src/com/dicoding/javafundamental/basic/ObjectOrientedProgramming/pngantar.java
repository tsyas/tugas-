package com.dicoding.javafundamental.basic.ObjectOrientedProgramming;

public class pngantar {
//    - Class dan Object
//       Kelas (terjemahan dari class) dan obyek (terjemahan dari object) adalah hal paling dasar yang harus kita pelajari.
//       Kita akan belajar bagaimana membuat suatu kelas dan menggunakaanya di dalam suatu program.
//    - Method
//       Suatu metode mendefinisikan perilaku obyek yang dibuat dari kelas. Metode dapat berupa prosedur atau fungsi.
//    - Member
//       Di materi ini kita akan belajar lebih dalam lagi tentang member (semua yang dideklarasikan) di dalam suatu kelas.
//    - Access Modifier
//       Di sini kita akan belajar tentang hak akses dari suatu kelas, metode, dan data member. Ada beberapa macam modifier
//       yaitu private, default, protected, dan public.
//    - Encapsulation
//       Encapsulation adalah metode untuk membundling suatu data dan metode di dalam kelas. Di dalamnya juga akan ada
//       materi tentang bagaimana mengaksesnya dengan memanfaatkan metode di dalam kelas.
//    - Inheritance
//       Inheritance adalah pewarisan, suatu kelas dapat diturunkan dari kelas lain sehingga mewarisi data dan
//       metode dari kelas lain tersebut. Materi inheritance sangat penting di dalam OOP.
//    - Interface
//       Selain inheritance, kita juga akan belajar tentang interface. Interface mirip seperti kelas,
//       tetapi hanya berisi method signature (nama, parameter dan return).
}
